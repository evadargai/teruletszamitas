    public void actionPerformed(ActionEvent actionEvent) {                                         
        float oldal1;
        float oldal2;
        String parancs;
   
        parancs=actionEvent.getActionCommand();
         if(parancs=="teruletTeglalap"){  
            oldal1=Float.valueOf(teglalapOldal1TF.getText());
            oldal2=Float.valueOf(teglalapOldal2TF.getText());
            teglalapEredmenyTF.setText(String.valueOf(oldal1*oldal2));        
        }
        if(parancs=="teruletKor"){  
            oldal1=Float.valueOf(korSugarTF.getText());
            korEredmenyTF.setText(String.valueOf(oldal1*Math.PI));        
        }
    }
